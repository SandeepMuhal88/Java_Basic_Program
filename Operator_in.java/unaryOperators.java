public class unaryOperators {
    public static void main(String[] agrs){
        int p=5;
        int q=5;
        System.out.println(p++);
        System.out.println(p);;

        System.out.println(++q);
        System.out.println(q);

        int x=p++;
        int y=++q;
        
        System.out.println(x);
        System.out.println(y);

        System.out.println(p);
        System.out.println(q);
    }
    
}
