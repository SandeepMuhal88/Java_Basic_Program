// Java assignment operator
public class AssignmentOperator{

    public static void main(String[] agrs){
        int p=10;
        int q;
        q=p;
        System.out.println(q);
        p+=q;
        System.out.println(p);
        p-=q;
        System.out.println(p);
        p*=q;
        System.out.println(p);
        p/=q;
        System.out.println(p);
        p%=q;
        System.out.println(p);
    
    }
}